s = "Hello, World!"

length = len(s)

upper_case = s.upper()

lower_case = s.lower()

title_case = s.title()

find_hello = s.find("Hello")

replace_world = s.replace("World", "Python")

strip_spaces = s.strip()

split_string = s.split(",")

join_string = "-".join(["Hello", "World"])

is_alpha = s.isalpha()

is_digit = "123".isdigit()

is_space = "    ".isspace()

print(length)
print(upper_case)
print(lower_case)
print(title_case)
print(find_hello)
print(replace_world)
print(strip_spaces)
print(split_string)
print(join_string)
print(is_alpha)
print(is_digit)
print(is_space)
